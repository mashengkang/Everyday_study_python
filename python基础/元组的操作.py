# 元组 tuple
# 用途:记录多个值,当多个值没有更改的需求,此时元素更合适
# 定义方式:在()内用逗号分割开多个任意类型的值

t = (1, 2, '大海', (2, 3), [1, 2, 3])
print(t)
print(type(t))
print(len(t))
print(t[0])

# t[0] = 8
# print(t)
# 直接更改值不行,但是可以更改元素中列表中的值
t[4][0] = 8
print(t)  # (1, 2, '大海', (2, 3), [8, 2, 3])

# 元组是不能修改和添加元素的,所以元组是不可变类型
# 如果想修改可转换成列表
t = list(t)
t[0] = 8
print(tuple(t))